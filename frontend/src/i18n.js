export function t(key) {
  const parts = key.split('.');
  let obj = dict[currentLang];
  
  for (const part of parts) {
    if (!obj || obj[part] === undefined) {
      // Fallback to EN
      let fallbackObj = dict['en'];
      for (const p of parts) {
        if (!fallbackObj || fallbackObj[p] === undefined) return key;
        fallbackObj = fallbackObj[p];
      }
      return fallbackObj;
    }
    obj = obj[part];
  }
  return obj;
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('ai_strategy_hub_lang', lang);
  updateStaticDOM();
}

export function toggleLang() {
  setLang(currentLang === 'en' ? 'de' : 'en');
}

// Function to update any static DOM elements with data-i18n attribute
export function updateStaticDOM() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerText = t(key);
  });
  
  // Update toggle button UI
  const langIcon = document.getElementById('lang-icon');
  const langLabel = document.getElementById('lang-label');
  if (langIcon && langLabel) {
    langIcon.innerText = currentLang === 'en' ? '🇬🇧' : '🇩🇪';
    langLabel.innerText = currentLang === 'en' ? 'EN' : 'DE';
  }
}
